// Logical AND Operator //

let x = 2;

// both expressions are true //
console.log((x < 4) && (4 >= x));  

// only one expression is true //
console.log((x <= 4) && (2 == 4));  

// both expressions are false //
console.log((x > 4) && (x == 4));  